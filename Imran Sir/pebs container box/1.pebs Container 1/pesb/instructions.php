	          <div class="heading-section heading-section-wo-line pt-md-4 mb-5">
	          	<div class="ml-md-0">
		          	
		            <h2 class="mb-4">SUBMISSION</h2>
	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>Submitted manuscript must not have been published previously nor they should have been under considerations for publications. Further, submission to PESB will be deemed to imply that the manuscript will not be submitted elsewhere if accepted. All submission should be mailed either to the Editor or to the consulting Editor or both. Electronic submission of manuscript is strongly encouraged, provided that the text, tables, and figures, are included in a single Microsoft word file(preferably in Aerial,font size 12). All portions of the submission should be typed double- spaced on one side of white A4 paper with ample margins on all sides. The corresponding author should give the complete mailing address, telephone number, mobile number and e- mail address. Submit manuscript as E- mail address. Submit manuscript as E mail attachment to the PESB at pesb@gmail.com, k.dhirendra17@gmail.com, premkuprasad@gmail.com.  An acknowledgement mail will be mailed to the corresponding author within 3 days of receipt. Decision of the PESB is final in the matter of acceptability for publications. The corresponding author is responsible for ensuring that the articleâ€² s publication has been approved by all the co-authors.</p>
							
						</div>
					</div>
				</div>
			</div>
		</section>
		
