		            <h2 class="mb-4">General</h2>
	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>Physical and Environmental Science Bulletin covers all areas of basic and applied aspects of Physics, Chemistry, Mathematics, Life Sciences, Environmental Sciences, Medical Sciences, Behavioral Sciences, Engineering, Geology, Marine Science, Computer Science, Toxicology etc. It also welcomes the submission of manuscript that meet general criteria of significance and scientific excellence but a contribution must have one of these subjects as its focus and be of relevant interest to scientists for acceptability. Both original research papers and review articles on current topics are biannually published. Book reviews are usually solicited, but suggestions of book for review are welcome. All contributions undergo editorial scanning and a peer- reviewed.</p>
							
