		            <h2 class="mb-4">ABSTRACT</h2>
	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>The abstract, not exceeding 200 words should convey the essence of the contribution even to a non specialist reader. For an original research paper, the abstract should include a few sentences of background to the work, the rationale, and the main results and conclusions. Abbreviations are discouraged.</p>
							
						</div>
					</div>
				</div>
			</div>
