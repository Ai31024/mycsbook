	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>All tables should be numbered, serially in Arabic numerals in order of appearance. All figures should be numbered in Arabic numbers in order of appearances. Authors should take responsibility for neat and correct arrangements of multi part figures. Photograph should be sharp, high contrast, glossy prints, any labeling (such as arrows or letter symbols) should be clear and applied with a fine pen. Colours photograph can be accepted only when absolutely necessary. DNA, RNA and protein sequence will be treated as figures, and the instructions for line drawings apply.</p>
							
						</div>
					</div>
				</div>
