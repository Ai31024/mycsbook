		            <h2 class="mb-4">REVIEW PROCESS</h2>
	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>All manuscripts are reviewed by minimum of two(2)external reviewers, members of the Advisory Board, Editorial Board. The author may also suggest a minimum of two reviewers for the manuscript(PESB reserve the right to designate other reviewers). Decision will be made as rapidly as possible, and the journal strives to return at least two(2)reviewers assessment to authors within two months of authors submission. The PESB Board will re-review(for publications/approval) manuscript that are pending revisions by the author. It is the goal of the PESB to publish manuscript after submission as early as possible if accepted by the PESB board.</p>
							
						</div>
					</div>
				</div>
			</div>
		</section>
		
		
