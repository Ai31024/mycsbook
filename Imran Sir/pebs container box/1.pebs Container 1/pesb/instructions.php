		            <h2 class="mb-4">TITLE</h2>
	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>The Title should be brief but interesting and comprehensive to a non- specialist reader, and contain word useful for indexing. This page must also include full names of all the authors and affiliations (institutional addresses),the name of the corresponding authors should be asterisk (*) and the corresponding authors Phone, mobile, fax, and E-mail information should be provided. The journal encourages authors to have their e-mail addresses printed.</p>
							
						</div>
					</div>
				</div>
			</div>
