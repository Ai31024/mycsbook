	            </div>
	          </div>
	          <div class="pb-md-4">
							<p>Authors should follow internationally accepted conventions in regard to units, symbols, and aberrations.SI units of measurements and standard aberrations only should be used. Authors are urged to take great care in distinguishing between genotype and phenotype clearly in all sections of the manuscript, including tables and illustrations.</p>
							
						</div>
